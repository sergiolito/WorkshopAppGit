
public class Client {
	private int codCli;
	private String cliDNI;
	private String cliName;
	private String cliFirstName;

	public Client() {
	}

	public int getCodCli() {
		return codCli;
	}

	public void setCodCli(int codCli) {
		this.codCli = codCli;
	}

	public String getCliDNI() {
		return cliDNI;
	}

	public void setCliDNI(String cliDNI) {
		this.cliDNI = cliDNI;
	}

	public String getCliName() {
		return cliName;
	}

	public void setCliName(String cliName) {
		this.cliName = cliName;
	}

	public String getCliFirstName() {
		return cliFirstName;
	}

	public void setCliFirstName(String cliFirstName) {
		this.cliFirstName = cliFirstName;
	}
}